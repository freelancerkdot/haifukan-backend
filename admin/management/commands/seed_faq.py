class Command(BaseCommand):
    help = "Seed FAQ items from the formerly-static /question page."

    def handle(self, *args, **options):
        created = 0
        for entry in FAQ_DATA:
            _, was_created = FaqItem.objects.update_or_create(
                order=entry["order"],
                defaults={
                    "category": entry["category"],
                    "question": entry["question"],
                    "answer": entry["answer"],
                    "is_active": True,
                },
            )
            if was_created:
                created += 1
        self.stdout.write(
            self.style.SUCCESS(f"Seeded {created} new FAQ items ({len(FAQ_DATA)} total).")
        )
